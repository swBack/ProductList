        return UIColor(displayP3Red: 236/255, green: 94/255, blue: 101/255, alpha: 1)
    }
    
    static var text_primary: UIColor {
        return UIColor.black
    }
    
    static var text_secondary: UIColor {
        return UIColor(displayP3Red: 119/255, green: 119/255, blue: 119/255, alpha: 1)
    }
}
